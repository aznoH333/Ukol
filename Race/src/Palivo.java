public class Palivo {

    int vyhrevnost;
    int pocet;

    public Palivo(int vyhrevnostC,int pocetC){
        vyhrevnost = vyhrevnostC;
        pocet = pocetC;
    }

    public int getVyhrevnost() {
        return vyhrevnost;
    }

    public int getPocet() {
        return pocet;
    }
}
